#include "libft.h"

void	pune_piesa(t_etris *p, char **map, int i, int j)
{
	int		k;

	k = 0;
	while (k < 8)
	{
		map[i + p->piesa[k] - '0'][j + p->piesa[k + 1] - '0'] = p->litera;
		k += 2;
	}
}
